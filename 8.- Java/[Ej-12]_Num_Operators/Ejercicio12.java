
public class Ejercicio12 {
	public static void main(String[] args) {
		int num = 25;
		System.out.println("Valor inical de num = 25");
		num += 5;
		System.out.println("Sumamos 5 a num = " + num);
		num -= 15;
		System.out.println("Restamos 15 a num = " + num);
		num *= 2;
		System.out.println("Multiplicamos num por 2 = " + num);
		num /= 2;
		System.out.println("Dividimos num entre 2 = " + num);
		num %= 3;
		System.out.println("Cogemos el resido de num entre 3 = " + num);
	}
}
